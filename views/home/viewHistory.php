					<h4 class="mb-2"><strong>Waffle House</strong></h4>
					<img style="max-height:200px;max-width:300px;padding:5px;float:right" src="images/WAF.jpg" >
					<p class="card-text">
						Waffle House is by far the most frequented of the Midnight Eats locations. While it may not be the prettiest of places, you can always count on issues being taken seriously here. Waffle House is not for the faint of heart. If it were, it would be called Waffle Apartment. When someone requests a meeting at Waffle House you can guarantee they are gonna lay out their feelings on the syrupy table. Sometimes tears are shed, other times blood boils over. It may be dealing with their own life or the life of someone close to them, but one thing is certain... big things are happening in their life and they need BIG talks to navigate through it.  It doesn’t matter if you’re Content Alex ordering smothered hash browns or Weird Hannah ordering two plates of bacon; the food is gonna destroy. You can be sure of one thing though, the conversation is gonna put you right back together.
					</p>
					<hr>
					<h4 class="mb-2"><strong>Steak n’ Shake</strong></h4>
					<img style="max-height:200px;max-width:300px;padding:5px;float:right" src="images/SAS.jpg" >
					<p class="card-text">
						Steak n’ Shake as Midnight Eats real estate is home to two things; horrible service and thought provoking conversation. When pulling into Steak n’ Shake you know not to expect anything good. You’re gonna be in there for at least 2 hours, your food is gonna be cold and soggy, and everyone is gonna be at least a little tired from the long drive. Once seated, the conversation can begin as patrons attempt to answer life’s most thought provoking and morbid questions from their own point of view. Many of the questions and events discussed are so far fetched that deep down we know they won’t happen. However, we also know that we must give an answer so we are prepared in case they do. What would you do if Anthony killed your parents? How would you deal with Carter not loving you? These are the types of questions that haunt all of us. We know these situations would never arise, but what if they did? This is the beating heart of Steak n’ Shake.
					</p>
					<hr>
					<h4 class="mb-2"><strong>International House of Pancakes</strong></h4>
					<img style="max-height:200px;max-width:300px;padding:5px;float:right" src="images/IHP.jpg" >
					<p class="card-text">
						IHOP is reserved for only the classiest of Midnight Eats. This is due in part to its high prices, but also the conversation that takes place. If you’re looking for a $6 burger, I suggest you try somewhere else. At IHOP burgers are in the $9-$10 range. It’s also a pancake house, despite the brief stint it served as a burger joint. The conversation at IHOP tends to be more lighthearted (unless you’re Mr. Family Man) than the previously mentioned Midnight Eats locations. This is simply the nature of the beast. Everyone understands that painful conversation happens over extremely cheap food because you’re not gonna be able to taste it anyway. IHOP’s above average pricing, therefore, leads to more lighthearted topics. Sometimes that is necessary though. Sometimes the stresses of life take their toll on us and we just want a brother to put his arm on our shoulder and tell us a good story. That is IHOP’s niche. That is why it must stay protected as one of the sacred locations. 
					</p>					
